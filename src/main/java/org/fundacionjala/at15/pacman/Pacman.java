package org.fundacionjala.at15.pacman;


public class Pacman {
    private int posX;
    private int posY;

    public Pacman() {
        posX = 1;
        posY = 1;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }


}
